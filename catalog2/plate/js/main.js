$(".box4").mouseenter(function(){
    $(".box4").css({height: "50%"});
    $(".box4").css({width: "50%"});
    $("box4").animate({position:"relative"})
    $("box4").animate({top:"60px"})
});

$(".box4").mouseleave(function(){
    $(".box4").css({height: "100%"});
    $(".box4").css({width: "100%"});
    
    
});

